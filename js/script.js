// 5. HÀM GỢI Ý SẢN PHẨM TƯƠNG TỰ
// ==========================================
function renderRelatedProducts(currentProduct) {
    const container = document.getElementById("related-products-container");
    if (!container) return;

    let sourceArray = Category_Animals;
    let isCurrentAnimal = Category_Animals.some(item => item.id === currentProduct.id);
    
    if (!isCurrentAnimal) {
        sourceArray = Category_Accessories;
    }

    let related = sourceArray.filter(item => item.id !== currentProduct.id);
    related.sort(() => 0.5 - Math.random());
    const recommendations = related.slice(0, 3);

    if (recommendations.length === 0) {
        container.innerHTML = `<div class="col-12 text-muted">Không có sản phẩm tương tự.</div>`;
        return;
    }

    container.innerHTML = recommendations.map(item => {
        const healthBadge = item.status ? `<span class="badge badge-success mb-2">Tình trạng: ${item.status}</span>` : '';
        return `
            <div class="col-12 col-md-4 mb-4">
                <div class="card h-100 shadow-sm border-0" style="border-radius: 20px; overflow: hidden;">
                    <img src="${item.image}" alt="${item.name}" style="width: 100%; height: 200px; object-fit: cover;">
                    <div class="card-body d-flex flex-column">
                        <h5 class="font-weight-bold text-dark" style="font-size: 1.1rem;">${item.name}</h5>
                        <p class="text-danger font-weight-bold">${item.price}</p>
                        ${healthBadge}
                        <p class="card-text text-muted small text-truncate">${item.description1 || ''}</p>
                        <div class="mt-auto d-flex gap-2">
                            <a href="${item.link}?id=${item.id}" class="btn btn-outline-danger flex-grow-1 btn-sm">Chi tiết</a>
                            <button onclick="addToCart('${item.id}')" class="btn btn-danger btn-sm">
                                <i class="fas fa-shopping-cart"></i>
                            </button>
                        </div>
                    </div>
                </div>
            </div>
        `;
    }).join('');
}

