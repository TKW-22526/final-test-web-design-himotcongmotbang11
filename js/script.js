const searchInput = document.getElementById("search-input");
if (searchInput) {
    searchInput.addEventListener("input", function() {
        const keyword = this.value.toLowerCase();
        const filteredAnimals = Category_Animals.filter(item => item.name.toLowerCase().includes(keyword));
        const filteredAccessories = Category_Accessories.filter(item => item.name.toLowerCase().includes(keyword));
        
        renderProducts(filteredAnimals, "animal-list");
        renderProducts(filteredAccessories, "accessory-list");
    });
}

const filterSelect = document.getElementById('filter-tag');
if (filterSelect) {
    filterSelect.addEventListener('change', function() {
        const value = this.value; // 'all', 'chim', 'ga-canh', 'long-chim', 'thuc-an'
        
        let filteredAnimals = Category_Animals;
        let filteredAccessories = Category_Accessories;

        if (value === 'chim') {
            filteredAnimals = Category_Animals.filter(item => {
                const numId = parseInt(item.id.replace('a', ''));
                return numId >= 1 && numId <= 38;
            });
            filteredAccessories = [];
        } else if (value === 'ga-canh') {
            filteredAnimals = Category_Animals.filter(item => item.name.includes('Gà'));
            filteredAccessories = [];
        } else if (value === 'long-chim') {
            filteredAnimals = [];
            filteredAccessories = Category_Accessories.filter(item => item.id >= 'acc1' && item.id <= 'acc6');
        } else if (value === 'thuc-an') {
            filteredAnimals = [];
            filteredAccessories = Category_Accessories.filter(item => item.id >= 'acc7' && item.id <= 'acc14');
        } else if (value === 'all') {
            filteredAnimals = Category_Animals;
            filteredAccessories = Category_Accessories;
        }

        renderProducts(filteredAnimals, "animal-list");
        renderProducts(filteredAccessories, "accessory-list");
    });
}

